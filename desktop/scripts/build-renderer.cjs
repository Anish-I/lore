#!/usr/bin/env node
// Precompile the renderer's JSX ahead of time so the shipped app needs neither
// @babel/standalone nor a CSP that allows 'unsafe-eval'/'unsafe-inline'.
//
//   renderer/index.src.html   (SOURCE: text/babel <script>s, editable)
//        │  Babel.transform(preset react)
//        ▼
//   renderer/compiled/*.js     (generated, plain JS)
//   renderer/index.html        (generated: loads compiled/*.js, script-src 'self')
//
// Run via `npm run build:renderer` (also wired into prestart/predist).
const fs = require('fs');
const path = require('path');

const RENDERER = path.join(__dirname, '..', 'renderer');
const SRC_HTML = path.join(RENDERER, 'index.src.html');
const OUT_HTML = path.join(RENDERER, 'index.html');
const OUT_DIR = path.join(RENDERER, 'compiled');

const Babel = require(path.join(RENDERER, 'vendor', 'babel.min.js'));

function compile(code, filename) {
  return Babel.transform(code, {
    presets: ['react'],
    filename,
    // Compact-but-readable; no source maps (renderer log already maps errors).
    retainLines: true,
  }).code;
}

function main() {
  fs.mkdirSync(OUT_DIR, { recursive: true });
  // Clear stale compiled output so renamed/removed sources don't linger.
  for (const f of fs.readdirSync(OUT_DIR)) {
    if (f.endsWith('.js')) fs.unlinkSync(path.join(OUT_DIR, f));
  }

  let html = fs.readFileSync(SRC_HTML, 'utf8');
  const compiled = [];

  // 1) Compile each `<script type="text/babel" ... src="X.jsx"></script>`.
  const externalRe = /<script\b[^>]*\btype=["']text\/babel["'][^>]*\bsrc=["']([^"']+)["'][^>]*><\/script>\s*/g;
  html = html.replace(externalRe, (_m, src) => {
    const abs = path.join(RENDERER, src);
    const base = path.basename(src).replace(/\.(jsx|js)$/i, '');
    const outName = `${base}.js`;
    fs.writeFileSync(path.join(OUT_DIR, outName), compile(fs.readFileSync(abs, 'utf8'), abs));
    compiled.push(`compiled/${outName}`);
    return `  <script src="compiled/${outName}"></script>\n`;
  });

  // 2) Compile the inline `<script type="text/babel"> ... </script>` bootstrap.
  const inlineRe = /<script\b[^>]*\btype=["']text\/babel["'][^>]*>([\s\S]*?)<\/script>\s*/g;
  html = html.replace(inlineRe, (_m, body) => {
    fs.writeFileSync(path.join(OUT_DIR, '_bootstrap.js'), compile(body, 'bootstrap.jsx'));
    compiled.push('compiled/_bootstrap.js');
    return `  <script src="compiled/_bootstrap.js"></script>\n`;
  });

  // 3) Drop the now-unused in-browser Babel runtime.
  html = html.replace(/\s*<script\s+src=["']vendor\/babel\.min\.js["']><\/script>/g, '');

  // 4) Tighten the CSP: no remote hosts, no eval, no inline scripts — everything is
  //    a local 'self' file now. (style-src keeps 'unsafe-inline' for the <style> block.)
  html = html.replace(
    /<meta http-equiv="Content-Security-Policy"[\s\S]*?">/,
    `<meta http-equiv="Content-Security-Policy" content="
  default-src 'self';
  script-src 'self';
  style-src 'self' 'unsafe-inline';
  img-src 'self' data: blob:;
  font-src 'self' data:;
  connect-src 'self' http://localhost:* http://127.0.0.1:*;
  object-src 'none';
  base-uri 'self';
  form-action 'none';
  frame-src 'none';
">`);

  // Refresh the CSP explainer comment.
  html = html.replace(
    /<!-- Content-Security-Policy[\s\S]*?-->/,
    `<!-- Content-Security-Policy. GENERATED FILE — edit index.src.html + run
     \`npm run build:renderer\`. All runtime libs + fonts are vendored locally and
     JSX is precompiled (renderer/compiled/), so script-src is a strict 'self':
     no CDN, no eval, no inline scripts. -->`);

  // Mark the generated file so nobody hand-edits it.
  html = html.replace('<!DOCTYPE html>',
    '<!DOCTYPE html>\n<!-- GENERATED by scripts/build-renderer.cjs from index.src.html — do not edit. -->');

  fs.writeFileSync(OUT_HTML, html);
  console.log(`build-renderer: compiled ${compiled.length} scripts -> renderer/compiled/, wrote index.html (script-src 'self')`);
}

main();
